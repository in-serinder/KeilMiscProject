/***************************************************************************************************
 * 函数名: SSD13XX_WriteChar
 * 描述: 在指定位置显示单个字符
 * 输入: x, y - 字符显示起始坐标
 *       c - 要显示的字符
 *       size - 字符大小(FONT_8X8或FONT_16X16)
 * 输出: 无
 * 返回: 无
 * 注意: 支持8x8和16x16两种字体大小，自动处理跨页显示
 **************************************************************************************************/
void SSD13XX_WriteChar(unsigned char x, unsigned char y, char c,
                       FontSize size) {
  unsigned char page, col, i;
  const unsigned char *font;
  if (c < ' ' || c > '~')
    c = ' ';

  if (size == FONT_8X8) {
    if (y % 8 != 0)
      return; // 必须页对齐
    page = y / 8;
    col = x;
    font = SSD13xx_Font8x8[c - ' '];
    SSD13XX_WriteCommand(0xB0 | page);
    SSD13XX_WriteCommand(col & 0x0F);
    SSD13XX_WriteCommand(0x10 | (col >> 4));
    for (i = 0; i < 8; i++) {
      SSD13XX_WriteData(font[i]);
    }
  } else if (size == FONT_16X16) {
    if (y % 8 != 0)
      return; // 16x16 也要求页对齐，避免跨更多页
    page = y / 8;
    col = x;
    // font = SSD13xx_Font16x16[c - ' '];
    font = SSD13xx_Font8x8[c - ' '];
    // 失能16x16字体，使用8x8字体显示 优化空间
    //  发送上半部分（第 0 ~ 7 行）
    SSD13XX_WriteCommand(0xB0 | page);
    SSD13XX_WriteCommand(col & 0x0F);
    SSD13XX_WriteCommand(0x10 | (col >> 4));
    for (i = 0; i < 8; i++) {
      SSD13XX_WriteData(font[i]);
    }
    // 发送下半部分（第 8 ~ 15 行）
    SSD13XX_WriteCommand(0xB0 | (page + 1));
    SSD13XX_WriteCommand(col & 0x0F);
    SSD13XX_WriteCommand(0x10 | (col >> 4));
    for (i = 8; i < 16; i++) {
      SSD13XX_WriteData(font[i]);
    }
  }
}
